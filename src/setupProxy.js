const { createProxyMiddleware } = require('http-proxy-middleware')
const { apiBaseUrl } = require('./services/api')

module.exports = function (app) {
  app.use(
    // @ts-ignore
    createProxyMiddleware('/api', {
      target: apiBaseUrl,
      changeOrigin: true,
      pathRewrite: { '^/api': '' }
    }),
  )
}
